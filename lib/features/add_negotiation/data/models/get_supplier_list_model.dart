import 'package:tradologie_app/features/add_negotiation/domian/enitities/supplier_list.dart';

class SupplierListModel extends SupplierList {
  const SupplierListModel({
    super.vendorId,
    super.vendorUserId,
    super.vendorCode,
    super.vendorShortName,
    super.vendorLogo,
    super.webLink,
    super.webUrl,
    super.vendorName,
    super.fileType,
    super.companyName,
    super.vendorDescription,
    super.vendorDocumentType,
    super.priority,
    super.annualTurnOver,
    super.yearOfEstablishment,
    super.certifications,
    super.areaOfOperation,
    super.manufacturer,
    super.membershipTypeId,
    super.trader,
    super.verified,
    super.rating,
    super.countryId,
    super.countryName,
    super.shortlistId,
    super.membershipTypeImage,
  });

  factory SupplierListModel.fromJson(Map<String, dynamic> json) =>
      SupplierListModel(
        vendorId: json["VendorID"],
        vendorUserId: json["VendorUserID"],
        vendorCode: json["VendorCode"],
        vendorShortName: json["VendorShortName"],
        vendorLogo: json["VendorLogo"],
        webLink: json["WebLink"],
        webUrl: json["WebURL"],
        vendorName: json["VendorName"],
        fileType: json["FileType"],
        companyName: json["CompanyName"],
        vendorDescription: json["VendorDescription"],
        vendorDocumentType: json["VendorDocumentType"],
        priority: json["Priority"],
        annualTurnOver: json["AnnualTurnOver"],
        yearOfEstablishment: json["YearOfEstablishment"],
        certifications: json["Certifications"],
        areaOfOperation: json["AreaOfOperation"],
        manufacturer: json["Manufacturer"],
        membershipTypeId: json["MembershipTypeID"],
        trader: json["Trader"],
        verified: json["Verified"],
        rating: json["Rating"],
        countryId: json["CountryID"],
        countryName: json["CountryName"],
        shortlistId: json["ShortlistID"],
        membershipTypeImage: json["MembershipTypeImage"],
      );
}
