import 'package:tradologie_app/features/authentication/domain/entities/fmcg_user_detail.dart';

class FmcgUserDetailModel extends FmcgUserDetail {
  const FmcgUserDetailModel({
    super.loginId,
    super.userId,
    super.accountType,
    super.titleId,
    super.titleName,
    super.fullName,
    super.genderId,
    super.genderName,
    super.mobile,
    super.email,
    super.dob,
    super.apiVerificationCode,
    super.brandId,
    super.fromDate,
    super.toDate,
    super.analyticsUrl,
    super.brandName,
    super.countryCode,
    super.countryID,
    super.businessType,
    super.productCategory,
    super.partnershipType,
    super.serviceLabel,
    super.exportingProducts,
  });

  factory FmcgUserDetailModel.fromJson(Map<String, dynamic> json) =>
      FmcgUserDetailModel(
        loginId: json["LoginID"].toString(),
        userId: json["UserID"].toString(),
        accountType: json["AccountType"].toString(),
        titleId: json["TitleID"].toString(),
        titleName: json["TitleName"].toString(),
        fullName: json["FullName"].toString(),
        genderId: json["GenderID"].toString(),
        genderName: json["GenderName"].toString(),
        mobile: json["Mobile"].toString(),
        email: json["Email"].toString(),
        dob: json["DOB"].toString(),
        apiVerificationCode: json["ApiVerificationCode"].toString(),
        brandId: json["BrandID"].toString(),
        fromDate: json["FromDate"].toString(),
        toDate: json["ToDate"].toString(),
        analyticsUrl: json["Analytics_URL"].toString(),
        brandName: json["BrandName"].toString(),
        countryCode: json["CountryCode"].toString(),
        countryID: json["CountryID"].toString(),
        businessType: json["BusinessType"].toString(),
        productCategory: json["ProductCategory"].toString(),
        partnershipType: json["PartnershipType"].toString(),
        serviceLabel: json["ServiceLabel"].toString(),
        exportingProducts: json["ExportingProducts"].toString(),
      );
}
