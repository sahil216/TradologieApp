import 'package:tradologie_app/features/dashboard/domain/entities/get_vendor_stock_list.dart';

class GetVendorStockListModel extends GetVendorStockList {
  const GetVendorStockListModel({
    super.requirementId,
    super.commodityId,
    super.commodityName,
    super.subCommodityId,
    super.subCommodityName,
    super.attribute1Id,
    super.attribute1Type,
    super.attribute2Id,
    super.attribute2Type,
    super.attribute1Value,
    super.attribute2Value,
    super.quantity,
    super.quantityUnit,
    super.locations,
    super.remarks,
    super.vendorId,
    super.vendorName,
    super.countryCode,
    super.mobileNo,
    super.email,
    super.insertedDate,
  });

  factory GetVendorStockListModel.fromJson(Map<String, dynamic> json) =>
      GetVendorStockListModel(
        requirementId: json["RequirementID"],
        commodityId: json["CommodityID"],
        commodityName: json["CommodityName"],
        subCommodityId: json["SubCommodityID"],
        subCommodityName: json["SubCommodityName"],
        attribute1Id: json["Attribute1ID"],
        attribute1Type: json["Attribute1Type"],
        attribute2Id: json["Attribute2ID"],
        attribute2Type: json["Attribute2Type"],
        attribute1Value: json["Attribute1Value"],
        attribute2Value: json["Attribute2Value"],
        quantity: json["Quantity"],
        quantityUnit: json["QuantityUnit"],
        locations: json["Locations"],
        remarks: json["Remarks"],
        vendorId: json["VendorID"],
        vendorName: json["VendorName"],
        countryCode: json["CountryCode"],
        mobileNo: json["MobileNo"],
        email: json["Email"],
        insertedDate: json["InsertedDate"],
      );

  Map<String, dynamic> toJson() => {
        "RequirementID": requirementId,
        "CommodityID": commodityId,
        "CommodityName": commodityName,
        "SubCommodityID": subCommodityId,
        "SubCommodityName": subCommodityName,
        "Attribute1ID": attribute1Id,
        "Attribute1Type": attribute1Type,
        "Attribute2ID": attribute2Id,
        "Attribute2Type": attribute2Type,
        "Attribute1Value": attribute1Value,
        "Attribute2Value": attribute2Value,
        "Quantity": quantity,
        "QuantityUnit": quantityUnit,
        "Locations": locations,
        "Remarks": remarks,
        "VendorID": vendorId,
        "VendorName": vendorName,
        "CountryCode": countryCode,
        "MobileNo": mobileNo,
        "Email": email,
        "InsertedDate": insertedDate,
      };
}
